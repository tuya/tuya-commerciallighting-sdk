package com.thingclips.smart.commercial.lighting.demo.bean;

public enum UpgradeStatusEnum {

    UPGRADING(0),

    NO_UPGRADE(1),

    HAS_FORCE_OR_REMIND_UPGRADE(2),

    HAS_CHECK_UPGRADE(3);
    private int type;

    UpgradeStatusEnum(int type) {
        this.type = type;
    }

    public static UpgradeStatusEnum to(int type) {
        for (UpgradeStatusEnum mode : values()) {
            if (mode.type == type) {
                return mode;
            }
        }
        return null;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }


}
